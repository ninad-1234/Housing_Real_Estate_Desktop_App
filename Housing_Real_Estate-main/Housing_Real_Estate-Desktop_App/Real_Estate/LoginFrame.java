    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try
        {
            String pwd_new = String.valueOf(txtN.getPassword()); //Converts a password into string
            String pwd_confirm = String.valueOf(txtC.getPassword()); //Converts a password into string
            if(pwd_new.equals("") || pwd_confirm.equals(""))
            {
                JOptionPane.showMessageDialog(null, "Fields can't be empty");
                return;
            }
            if(!pwd_new.equals(pwd_confirm)) {
                JOptionPane.showMessageDialog(null, "Both the passwords do not match");
            }
            else 
            {
                PreparedStatement pst = (PreparedStatement) con.prepareStatement("Update user_info set Password=? where  Username=?");
                pst.setString(1, pwd_confirm);
                pst.setString(2, userid);
                int r = pst.executeUpdate(); 
                if(r>0) //Result set value if true
                {
                    JOptionPane.showMessageDialog(null, "Updated successfully!!!");
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "Incorrect userid.");
                }
            }
        }
        catch(Exception e1) {
            e1.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

